"""
Python contain few basic types for storage.
"""

#Number
number1 = 5 #To define a number simply type the number, declearing an integer
number2 = 3.14 #It can also have decimal point, declearing a float
print(number1)

#String
string1 = "Hello" #To define a string wrap it around quotes signs
string2 = 'Hello' #You can also use single quotes signs
print(string1)

#Boolean
#Can be either true or false
bool1 = True
bool2 = False
print(bool1)

#You can also use the 'not' flag to flip a condition
bool3 = not True #will set false